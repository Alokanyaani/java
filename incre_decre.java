public class incre_decre {
  public static void main(String[] args){
    int i = 0;
    
    System.out.println(i);//0 --0
    System.out.println(i++);//0--1 
    System.out.println(i++);//1 --2
    System.out.println(i);//2
    System.out.println(i--);//2 -- 1
    System.out.println(--i);//0 -- 0
    System.out.println(i);//0
  }  
}
